                    gen_to_be_invoked.SampleInput( ref _command, _deltaTime );
                    translator.Push(L, _command);
                        translator.Update(L, 2, _command);
                        
                    
                    
                    
                    return 1;
                }
                
            } catch(System.Exception gen_e) {
                return LuaAPI.luaL_error(L, "c# exception:" + gen_e);
            }
            
        }
        
        
        
        
        
        
		
		
		
		
